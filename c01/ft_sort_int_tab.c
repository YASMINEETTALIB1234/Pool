#include <unistd.h>

void	ft_sort_int_tab(int *tab, int size)
{
	int	i;

	i = 0;
	j = size - 1;
	while (i <= j)
	{
		if (tab[i] > tab[i + 1])
		{
			tab[i] = tab[i + 1];
		}
		i++;
	}
}
